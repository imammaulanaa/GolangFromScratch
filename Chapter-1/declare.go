package main

import "fmt"

func main() {
	// Mendeklarasikan variabel integer dengan nilai 10
	var angkaInteger int = 10

	// Mendeklarasikan variabel float dengan nilai 3.14
	var angkaFloat float32 = 3.14

	// Mendeklarasikan variabel string dengan nilai "Hi, Imam!"
	var teks string = "Hi, Imam!"

	// Mendeklarasikan variabel boolean dengan nilai true
	var benar bool = true

	// Mencetak nilai variabel ke konsol
	fmt.Println("Nilai integer:", angkaInteger)
	fmt.Println("Nilai float:", angkaFloat)
	fmt.Println("Nilai string:", teks)
	fmt.Println("Nilai boolean:", benar)
}
